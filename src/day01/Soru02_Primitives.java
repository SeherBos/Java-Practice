package day01;

public class Soru02_Primitives {

	public static void main(String[] args) {

			/* primitive tipler;
			 * boolean,char,byte,short,int,long,float,double
			 * max ve min degerlerini yazdirin
			 * */
		
		System.out.println("byte max degeri: " + (Byte.MAX_VALUE));
		
		System.out.println("byte min degeri: " + (Byte.MIN_VALUE));

		System.out.println("Short max degeri: " +(Short.MAX_VALUE));

		System.out.println("Short min degeri: " + (Short.MIN_VALUE));

		System.out.println("Integer max degeri: " + (Integer.MAX_VALUE));

		System.out.println("Integer min degeri: " +(Integer.MIN_VALUE));
		
		System.out.println("Long max degeri: " + (Long.MAX_VALUE ));

		System.out.println("Long max degeri: " + (Long.MIN_VALUE));
		
		System.out.println("Float max degeri: " +(Float.MAX_VALUE));

		System.out.println("Float min degeri: " + (Float.MIN_VALUE));
		
		System.out.println("Double max degeri: " +(Double.MAX_VALUE));

		System.out.println("Double min degeri: " + (Double.MIN_VALUE));
	}

}
